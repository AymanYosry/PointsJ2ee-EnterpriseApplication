package com.ewhale.points.common.exception;

import com.ewhale.points.common.util.ExceptionConstants;

/**
 * @author Ayman Yosry
 */
public class ValidationException extends ApplicationException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 */
	public ValidationException()
	{
		super(ExceptionConstants.VALIDATION_EXCETION_STATUS_CODE);
	}

	/**
	 * @param message
	 */
	public ValidationException(String message)
	{
		super(message, ExceptionConstants.VALIDATION_EXCETION_STATUS_CODE);
	}

	/**
	 * @param cause
	 */
	public ValidationException(Throwable cause)
	{
		super(cause, ExceptionConstants.VALIDATION_EXCETION_STATUS_CODE);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public ValidationException(String message, Throwable cause)
	{
		super(message, cause, ExceptionConstants.VALIDATION_EXCETION_STATUS_CODE);
	}
}
