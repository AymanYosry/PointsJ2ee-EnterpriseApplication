package com.ewhale.points.common.exception;

import com.ewhale.points.common.util.ExceptionConstants;

/**
 * @author Ayman Yosry
 */
public class TimerException extends ApplicationException
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;


	/**
	 * 
	 */
	public TimerException()
	{
		super(ExceptionConstants.TIMER_EXCETION_STATUS_CODE);
	}

	/**
	 * @param message
	 */
	public TimerException(String message)
	{
		super(message, ExceptionConstants.TIMER_EXCETION_STATUS_CODE);
	}

	/**
	 * @param cause
	 */
	public TimerException(Throwable cause)
	{
		super(cause, ExceptionConstants.TIMER_EXCETION_STATUS_CODE);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public TimerException(String message, Throwable cause)
	{
		super(message, cause, ExceptionConstants.TIMER_EXCETION_STATUS_CODE);
	}
}
